LoanwordData parseLoanwordJson(const std::string& label, const std::string& json) {
    nlohmann::json root;
    try {
        root = nlohmann::json::parse(json);
    } catch (const nlohmann::json::parse_error& e) {
        throw LoanwordSchemaError(label + ": invalid JSON: " + e.what());
    }
    if (!root.is_object()) {
        throw LoanwordSchemaError(label + ": top-level must be a JSON object");
    }

    LoanwordData data;
    // Lenient version handling for forward-compat (review feedback C-1, R-C4).
    // Match Python / Rust / C# / Go loaders:
    //   1. Accept ``version`` if present and numeric.
    //   2. Fall back to ``schema_version`` if ``version`` is absent.
    //   3. Silently default to 1 if neither is present or the value is not a
    //      number.
    // Previously this loader required ``version`` to be a non-negative
    // integer, which made a future ``schema_version: 2`` manifest fail to
    // load in C++/Go even though Rust/Python/C# silently accepted it.
    data.version = 1;
    auto vit = root.find("version");
    if (vit != root.end() && vit->is_number_integer()) {
        data.version = vit->get<int>();
    } else {
        auto svit = root.find("schema_version");
        if (svit != root.end() && svit->is_number_integer()) {
            data.version = svit->get<int>();
        }
    }

    parse_section(root, label, "acronyms", data.acronyms);
    parse_section(root, label, "loanwords", data.loanwords);
    parse_section(root, label, "letter_fallback", data.letter_fallback);

    return data;
}

std::shared_ptr<const LoanwordData> getDefaultLoanwordData() {
    std::call_once(g_default_init_flag, init_default);
    return g_default;
}

const std::string& getDefaultLoanwordError() {
    std::call_once(g_default_init_flag, init_default);
    return g_default_error;
}

std::shared_ptr<const LoanwordData> loadLoanwordDataFromPath(const std::string& path) {
    std::ifstream ifs(path);
    if (!ifs) {
        throw std::runtime_error("loanword: failed to open " + path);
    }
    std::ostringstream buf;
    buf << ifs.rdbuf();
    auto data = parseLoanwordJson(path, buf.str());
    return std::make_shared<const LoanwordData>(std::move(data));
}

