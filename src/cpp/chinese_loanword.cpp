// -------------------------------------------------------------------------
// JSON type helpers (for Python-equivalent error messages)
// -------------------------------------------------------------------------
const char* json_type_name(const nlohmann::json& v) {
    using T = nlohmann::json::value_t;
    switch (v.type()) {
    case T::null:           return "null";
    case T::boolean:        return "bool";
    case T::number_integer:
    case T::number_unsigned:
    case T::number_float:   return "number";
    case T::string:         return "string";
    case T::array:          return "list";
    case T::object:         return "dict";
    default:                return "unknown";
    }
}

void parse_section(const nlohmann::json& root,
                   const std::string& label,
                   const std::string& section,
                   std::unordered_map<std::string, std::vector<std::string>>& target) {
    auto it = root.find(section);
    if (it == root.end()) return;  // missing section ok (forward-compat)

    if (!it->is_object()) {
        std::ostringstream os;
        os << label << ": section '" << section << "' must be a mapping, got "
           << json_type_name(*it);
        throw LoanwordSchemaError(os.str());
    }

    for (auto sit = it->begin(); sit != it->end(); ++sit) {
        const auto& key = sit.key();
        const auto& val = sit.value();
        if (!val.is_array()) {
            std::ostringstream os;
            os << label << ": '" << section << "." << key
               << "' must be list[str], got " << val.dump();
            throw LoanwordSchemaError(os.str());
        }
        std::vector<std::string> items;
        items.reserve(val.size());
        for (const auto& elem : val) {
            if (!elem.is_string()) {
                std::ostringstream os;
                os << label << ": '" << section << "." << key
                   << "' must be list[str], got " << val.dump();
                throw LoanwordSchemaError(os.str());
            }
            items.push_back(elem.get<std::string>());
        }
        target.emplace(key, std::move(items));
    }
}

// -------------------------------------------------------------------------
// One-time default-data init (singleton)
// -------------------------------------------------------------------------
std::shared_ptr<const LoanwordData> g_default;
std::string g_default_error;
std::once_flag g_default_init_flag;

#ifndef PIPER_PLUS_EMBEDDED_LOANWORD
std::string get_default_loanword_path() {
    // Beside the executable, in the conventional dict_dir layout used by
    // the desktop installer (`<exe-dir>/data/zh_en_loanword.json`).
    std::array<char, 4096> buf{};
    if (piper_plus_get_exe_dir(buf.data(), static_cast<int>(buf.size())) == 0) {
        std::string dir(buf.data());
        return dir + "/data/zh_en_loanword.json";
    }
    return "data/zh_en_loanword.json";  // CWD-relative fallback
}
#endif

void init_default() {
    try {
#ifdef PIPER_PLUS_EMBEDDED_LOANWORD
        const std::string label = "zh_en_loanword.json (embedded)";
        const std::string json = load_embedded_json();
        auto data = parseLoanwordJson(label, json);
#else
        const std::string path = get_default_loanword_path();
        std::ifstream ifs(path);
        if (!ifs) {
            g_default_error = "failed to open " + path;
            return;
        }
        std::ostringstream buf;
        buf << ifs.rdbuf();
        auto data = parseLoanwordJson(path, buf.str());
#endif
        g_default = std::make_shared<const LoanwordData>(std::move(data));
    } catch (const std::exception& e) {
        g_default_error = e.what();
    }
}

